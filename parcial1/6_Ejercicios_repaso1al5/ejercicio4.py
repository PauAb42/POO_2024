#Solicitar 2 numeros al usuario y realizar todas las operaciones básicas de una calculadora (+,-,*,/) y mostrar por pantalla el resultado


numero1=int(input("Ingrese el primer número: "))
numero2=int(input("Ingrese el primer número: "))

suma=numero1+numero2
print(f"{numero1} + {numero2} = {suma}")

resta=numero1-numero2
print(f"{numero1} - {numero2} = {resta}")

multi=numero1*numero2
print(f"{numero1} X {numero2} = {multi}")

division=numero1/numero2
print(f"{numero1} / {numero2} = {division}")